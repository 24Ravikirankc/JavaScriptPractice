// Example array of colors
let colors = ["red", "green", "blue", "yellow"];

// Remove the last element from the array
colors.push("white");
colors.pop(); // remove the last element

// Log the updated array
console.log(colors);
colors.push("white"); // adding the element at last
console.log(colors);
colors.sort(); // arrange from alphabetically
console.log(colors);
colors.reverse(); // reverse
    console.log(colors);